
namespace LacoDeRepeticaoWHILE {
    
    class Program {

        static void Main(string[]args) {

            Console.WriteLine("");

            int i = 1;

            while (i < 10) {
                Console.WriteLine("Iteração: {0}", i);
                i++;
            }

            Console.WriteLine("\nPrograma finalizado!\n");

        }

    }

}